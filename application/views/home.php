	<h1>TaKDA</h1>
	<h2>Course Offering Management System</h2>

	<p>The system named “TaKDa“ is a course offering management system developed to provide an easy-to-implement course allocation and room control in the Institute of Computer Science. Due to the increasing number of students taking up Computer Science in UPLB, manual course allocations is seen to be inefficient. That is why a system that will address this problem is seen to be important.</p>
	
